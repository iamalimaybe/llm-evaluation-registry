CREATE TABLE evaluation_runs (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    workflow_id BIGINT NOT NULL,
    prompt_version_id BIGINT NOT NULL,
    evaluation_case_id BIGINT NOT NULL,

    model_name VARCHAR(120) NOT NULL,
    model_version VARCHAR(120),
    provider VARCHAR(80) NOT NULL DEFAULT 'OLLAMA',

    raw_output TEXT,
    parsed_output JSONB,

    confidence NUMERIC(5, 2),
    temperature NUMERIC(4, 2),
    run_config JSONB,

    status VARCHAR(30) NOT NULL DEFAULT 'PENDING',
    passed BOOLEAN,
    score NUMERIC(5, 2),
    failure_reasons JSONB,
    reviewer_notes TEXT,

    started_at TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP,
    completed_at TIMESTAMP,
    created_at TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP,
    updated_at TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP,

    CONSTRAINT fk_evaluation_runs_workflow
         FOREIGN KEY (workflow_id)
             REFERENCES ai_workflows (id),

    CONSTRAINT fk_evaluation_runs_prompt_version
         FOREIGN KEY (prompt_version_id)
             REFERENCES prompt_versions (id),

    CONSTRAINT fk_evaluation_runs_evaluation_case
         FOREIGN KEY (evaluation_case_id)
             REFERENCES evaluation_cases (id),

    CONSTRAINT chk_evaluation_runs_status
         CHECK (status IN ('PENDING', 'RUNNING', 'PASSED', 'FAILED', 'ERROR')),

    CONSTRAINT chk_evaluation_runs_score
         CHECK (score IS NULL OR (score >= 0 AND score <= 100)),
             CONSTRAINT chk_evaluation_runs_confidence
             CHECK (confidence IS NULL OR (confidence >= 0 AND confidence <= 100)),

    CONSTRAINT chk_evaluation_runs_temperature
        CHECK (temperature IS NULL OR (temperature >= 0 AND temperature <= 2))
);