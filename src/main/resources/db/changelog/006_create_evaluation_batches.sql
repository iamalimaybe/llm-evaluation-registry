CREATE TABLE evaluation_batches (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    workflow_id BIGINT NOT NULL,
    prompt_version_id BIGINT NOT NULL,
    model_name VARCHAR(120) NOT NULL,
    model_version VARCHAR(120),
    provider VARCHAR(80) NOT NULL,
    temperature NUMERIC(4, 2),
    run_config JSONB,
    status VARCHAR(40) NOT NULL,
    total_runs INTEGER NOT NULL DEFAULT 0,
    completed_runs INTEGER NOT NULL DEFAULT 0,
    passed_runs INTEGER NOT NULL DEFAULT 0,
    failed_runs INTEGER NOT NULL DEFAULT 0,
    errored_runs INTEGER NOT NULL DEFAULT 0,
    average_score NUMERIC(5, 2),
    failure_reasons JSONB,
    started_at TIMESTAMP,
    completed_at TIMESTAMP,
    cancel_requested_at TIMESTAMP,
    created_at TIMESTAMP NOT NULL,
    updated_at TIMESTAMP NOT NULL,
    CONSTRAINT fk_evaluation_batches_workflow
        FOREIGN KEY (workflow_id)
            REFERENCES ai_workflows (id),
    CONSTRAINT fk_evaluation_batches_prompt_version
        FOREIGN KEY (prompt_version_id)
            REFERENCES prompt_versions (id),
    CONSTRAINT chk_evaluation_batches_status
        CHECK (status IN (
                  'QUEUED',
                  'RUNNING',
                  'COMPLETED',
                  'COMPLETED_WITH_FAILURES',
                  'CANCEL_REQUESTED',
                  'CANCELLED',
                  'ERROR'
        ))
);

